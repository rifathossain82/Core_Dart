void main(){

  /// number
  /// int type
  int x=10;           // var x=10; it also work
  print("Integer value is = ${x}");

  /// number
  /// double type
  double d=10.50;           // var x=10.50; it also work
  print("Double value is = ${d}");

  /// string
  String s="Rifat Hossain";           // var s="Rifat Hossain"; it also work
  print("String value is = ${s}");

  /// boolean
  bool b=false;           // var b=false; it also work
  print("Boolean value is = ${b}");

  /// list
  List l= [1,2,3];           // var l=[1,2,3]; it also work
  print("List value is = ${l}");


  /// maps
  Map m= {"name":"Rifat hossain",'Roll':940279,'Department':'Computer Technology'};           //   var m= {"name":"Rifat hossain",'Roll':940279,'Department':'Computer Technology'};   it also work
  print("Maps value is = ${m.length}");


  /// Runes
  var heart_symbol = '\u2665';
  var laugh_symbol = '\u{1f600}';
  print(heart_symbol);
  print(laugh_symbol);
}