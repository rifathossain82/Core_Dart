void main(){
  ///to print the result based on the student's marks.
  var marks=74;
  print("Student's result : ");
  if(marks>=80){
    print("Excellent");
  }
  else if(marks>=70){
    print("Very Good");
  }
  else if(marks>=60){
    print("Good");
  }
  else if(marks>=40){
    print("Average");
  }
  else{
    print("Bad");
  }

}